　　二、耐心辅导，培养兴趣。

　　“兴趣是最好的老师”，一个人只有对某个问题产生兴趣，才会启动思维，才会主动去寻找解决的办法，才会主动获取相关的知识，将“要我学”变为“我要学”。兴趣是激发学生学习积极性的动力，也是激发创造力的必要条件。因此，在计算机教学过程中，如何激发学生的学习兴趣无疑是教师的重要任务之一。作为教师，不能因学生不具备基本的计算机知识和操作能力而嘲笑、训斥学生，一定要注意聆听学生提出的每个问题，并针对不同问题做出有效的解答。有的可直接了当地回答，有的需演示一遍操作过程，有的要和他们作简短的讨论，有的则要用另一个问题将学生的思考引向深入。

　　先生的责任不在于教书，不在于教学生，而是教会学生学习。教的法子要根据学的法子，这叫做“教学合一”。千教万教教人求真，千学万学学做真人。施教之功，贵在诱导，进学之功，贵在心悟。善思则得，善诱则通；诱思交融，百炼成钢。

