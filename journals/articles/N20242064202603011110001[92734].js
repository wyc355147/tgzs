　　【片段2：作文讲评】

　　通过评改，多数同学叙事清楚，过渡自然，主题明确，但一些学生对文章结尾深化主题还有待提高。

　　如：谭德旭同学的作文，文中写了两次谈话，一次是邻居的老爷爷、老奶奶，他们是留守老人，子女多年外出从未回家。自己的爷爷是一个木匠，经常帮助邻居的老爷爷老奶奶，在一次吃饭时“我”问爷爷，为什么要帮助邻居爷爷奶奶时，爷爷告诉“我”要做一个“对国家，对别人有用的人”。另一次是爸妈回家过年，一家人其乐融融，爸妈对“我”关爱有加，使我感到快乐。结尾处写到：餐桌前的那次谈话，让我至今想起了都有些开心，至今难忘。

　　师：同学们，我们来看谭德旭同学的结尾，有哪些问题？我们帮助他好吗？

　　生1：文中有“两次”谈话，但结尾处写到“那次谈话”，前后不对应。

　　生2：从语句上来看，两个“至今”重复了，可以把第二个“至今”去掉，改为“令人难忘”。

　　生3：结尾处应该收拢全篇，把两次谈话进行归纳，爷爷和我的谈话偏重于对我的教育，爸爸和我的谈话偏重于关心。是否可以改为“难忘爷爷对我的教育和爸爸对我的关心”。

　　师：同学们，从这篇文章的结尾进行总结一下，作文结尾应该注意什么？

　　生1：结尾处要注意总结全文。

　　生2：结尾处要注意引起读者的共鸣。

　　生3：结尾是全文的延续，不是故事的终结，要起到“余音绕梁，三日不绝”的效果。

　　师：同学们归纳得很好，其实作文结尾可以“借题发挥”，起到深化和升华文章主题的作用，提升作文的档次，在考试中增加作文的得分。“借题发挥”可以有以下几种方法：

　　（一）借“物”发挥。可以借文中的特定事物，加以发挥，引起读者的共鸣。如本文中多次出现的餐桌，在它的面前有爷爷的教导和爸爸的关心，它是作者的情感寄托，在篇末再次出现，极易引起读者的共鸣，给人一种意犹未尽的感觉。

　　（二）借“人”发挥。人是社会的产物，在人的身上有社会环境的印记，可以反映当时社会的人情冷暖。如上文中的邻居老爷爷老奶奶正是当今物欲横流环境下的悲剧人物。在文章结尾交代他们的结局，使读者看到社会的另一面，勾起读者的良知和深思。

　　（三）借“情”发挥。作文开篇往往奠定全文的感情基调，如本文的开头“又一次回到我的家乡，看着熟悉的老屋，摸着满是灰尘的餐桌，我的思绪不觉回到了三年前。”从这来看，“我”离开家乡应该是有三年时间了，为什么离开，老屋邻居老爷爷老奶奶最后怎样了，文中没有交代。从开头的字里行间可以品读出“我”淡淡的忧伤，这又是为何？可以从当前的城镇化和留守老人的角度进行结尾深化全文，引起读者共鸣。可以这样结尾：

