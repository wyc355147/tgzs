　　一、案例背景

　　本人从今年8月份起新接任七年级两个班的语文，通过几次作文训练，发现学生在作文时，选材较好，叙事清楚明了，但作文结尾显得很随意，不能很好的总结全文和深化文章主题。从学生写作能力的角度讲，这对提高作文水平很不利；从应试的角度讲，会导致学生作文得分不高。作为贵州省民族地区中小学教育高质量发展支持专项行动计划项目校的一名语文教师，如何指导学生写好作文结尾，深化文章主题，一直萦绕在我的心头。

