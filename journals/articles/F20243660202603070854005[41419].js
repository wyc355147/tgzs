　　一、案例背景

　　我校大多数学生都是留守儿童，从小缺少父母监管，在小学语文基础就不好，进入初中后，由于学生基础差，教师教学内容多，再加上教师把大部分精力花在对学生的作业指导上，导致学生没有时间去进行课外阅读，缺少写作素材，对学生较难的写作走形式，学生在写作文时存在立意不明确，思路不清晰、结构不清晰、文体不明确等问题，导致学生怕写作文，厌写作文，结合我校学情，教师可以充分挖掘学生熟悉的初中生必读的名著导读和课外书籍以及优秀作品，用于进行写作时的训练，来提高学生对写作的兴趣，从而让学生喜欢上写作文。

　　二、案例理念

　　阅读和写作是人类语言交流的两个基本方式，两者密不可分，相辅相成。阅读是获取信息和知识的重要途径，而写作则是表达思想和交流感情的重要方式。阅读和写作流程不同，但它们之间存在着某种必然的关系。阅读促进写作，阅读是写作的基础。课外阅读可以帮助学生了解世界和文化，学习知识和技能，扩展思维和表达能力。通过认课外书籍的阅读，对优秀作文的鉴赏，学生可以积累大量的词汇、语法和句式，以及各种文学和写作技巧，这些都可以帮助学生提升写作水平。

