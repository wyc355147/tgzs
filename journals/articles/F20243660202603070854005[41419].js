　　四、教学感悟

　　《语文课程标准》明确指出：“写作教学应贴近学生实际，让学生易于动笔，乐于表达，应引导学生关注课外书籍，热爱生活，表达真情实感”。作文教学中应重视阅读实践，就是让学生在平常的生活中体验课外书中所折射出来的酸甜苦辣，让学生身同感受其呈现出来的喜怒哀乐，使学生产生强烈的述说欲望，这时是教师引导学生写作的最佳时机，从而有效地将学生从被动的写作心理状态要“我写”，过渡到“我想写”“我要写”的主动写作心理状态中来。把学生眼中枯燥无味，头疼的作文课，通过阅读课的形式展开，让学生学会写作文，但要让学生从无从下笔到一气呵成，这也是一个漫长的教学过程。

　　首先，要抓学生的阅读，多读书，多感悟，多模仿，才能如源头活水，给作文教学与生机和活力，定期让学生阅读课外书籍，给学生推荐好文章，引导学生欣赏评价，既给学生提供写作素材，又能提高学生的欣赏水平。

　　其次，品读好文章、摘抄佳句好词。写作成功的同学大多有相似的经历：读到好文章，他们反复咀嚼甚至把好词佳句摘抄下来，熟读成诵。作文时一不小心用了上去，结果老师大加赞赏一次成功使他们更加投入地去阅读去积累，不知不觉中作文水平就见长。

　　第三，重视语言文字的训练。学生运用语言文字的能力较差，比如说常写错别字，写文章病句多，表达不清晰等等。本学期我注重了这方面的训练，训练时做到词不离句，句不离篇。再如对生字的教学方法多样，有比较法，有重点突破法，有归类法等等。

　　第四，在平日要注重对学生进行就课外阅读材料提炼观点、概括故事内容的训练，提高学生的提炼概括能力。

　　总之，只要在主观上重视学生的作文教学，多阅读，多思考，多实践，学生的写作能力定会有所提高的！
