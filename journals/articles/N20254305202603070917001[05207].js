　　学校场景：一所中学里，教学楼、图书馆、实验室等各个区域都需要稳定的网络连接，学校采用光纤接入为主干网络，各个教室和办公室通过有线网络连接到校园网，同时在校园内的一些公共区域如操场、食堂等设置了Wi- Fi热点，方便师生使用移动设备上网。

　　企业场景：一家科技公司，员工们需要进行大量的数据传输和在线协作，公司采用高速光纤接入，并为员工的办公电脑提供有线网络连接，同时为方便员工在会议室等场所使用移动设备进行展示和讨论，也配备了企业级的Wi- Fi网络。

