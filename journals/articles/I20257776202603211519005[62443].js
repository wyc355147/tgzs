　　在我的鼓励下，该小组重新整理了采访数据，还拍摄了违规停车的照片，制作成图文并茂的“问题报告”。当他们再次找到那位家长时，家长看着详细的报告，惊讶地说：“原来你们做了这么多准备，我之前错怪你们了，我支持你们的建议！”

　　（三）第三步：多方协商——表达诉求，感受“民主参与”的力量

