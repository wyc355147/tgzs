　　协商会上，各小组依次展示自己的建议方案。第一小组提出“在学校两侧50米内设置临时停靠区，上下学时段允许家长短暂停车，其他时间禁止停车”，并拿出了车流量统计数据，证明“临时停靠区”不会影响交通；第二小组建议“由公安特警和老师组成‘交通引导队’，上下学时段在路口引导交通”，还制定了详细的志愿者排班表；第三小组则结合《道路交通安全法》，提出“交警部门在上下学时段加强巡逻，对违规停车的车辆进行劝导或处罚”。

