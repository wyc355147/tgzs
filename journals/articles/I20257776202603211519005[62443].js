　　正如一位学生所说：“以前我觉得‘民主’离我很远，现在我知道，它就在我们身边——它是我们为改善交通提出的一条建议，是我们与叔叔阿姨协商时说的一句话，是我们看到校门口变通畅时的一个笑容。原来，我们每个人都可以成为‘民主生活’的参与者和推动者。”

　　这正是《参与民主生活》教学的真正意义——让学生在实践中成长，在参与中担当，成为有责任、有能力的现代公民。
