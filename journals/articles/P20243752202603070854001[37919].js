　一、案例背景
　
　　随着新课程改革的不断深入,小组合作学习已成为改革的基本理念之一。它在改善课堂氛围,帮助学生形成良好的学习品质等方面具有很好的效果,在初中物理教学中得到广泛的应用。合作学习是指学生在小组或团队中为了完成共同的学习任务,有明确的责任分工的互助性学习。与传统的教学方式相比,合作学习在培养学习的自学能力、发现问题与解决问题的能力方面都具有显著实效,而且能促进学生之间的相互交流、共同发展。因此,被人们誉为:"近十几年来最重要和最成功的教学改革!"

