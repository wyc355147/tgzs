　　一、探究实践的含义及在教学中的地位

　　在课程标准中对探究实践描述具体指的是源于对自然界的好奇心、求知欲和现实需求，解决真实情境中的问题或完成实践项目的能力与品格。探究实践活动主要包括科学探究和跨学科实践。主要环节有：发现问题或提出任务，制订方案，实施方案，获得证据或形成初步产品，分析证据或改进设计，得出结论或物化成果，进行表达、交流或展示等。从上面的描述可以看出开展实验教学的重要性。同时也是我们落实新课标的有效途径。实验是获取知识、认识事物，发展思维，体验教学的一种综合行为。从教学角度来说，探究实践是学生收获知识，提升能力，培育素养的主要途径之一。

