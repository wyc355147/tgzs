　　一、长征的原因

　　教师：从1930年12月到1933年1月，以蒋介石为首的国民党反动派先后对中央革命根据地进行了五次大规模的“围剿”。中央革命根据地，地处江西南部、福建西部，根据地面积约五万平方千米，是当时最大的一块革命根据地。

