　　教师：博古、李德等人不考虑中国的实际情况，导致红军损失惨重。经过广昌一战，红军又伤亡五千六百多人，中央革命根据地的北大门被打开了。之后，敌军向中央革命根据地内部进攻，直指中华苏维埃政府首都瑞金。由于博古、李德等人硬拼硬打、不计成本的作战方法（冒险主义错误），结果一败涂地。第五次反“围剿”失利，红军不得不退出中央革命根据地，进行战略转移——长征。

